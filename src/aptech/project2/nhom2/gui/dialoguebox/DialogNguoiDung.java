    private void btnAddUserActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAddUserActionPerformed
        if (txtFullName.getText().isEmpty() || txtFullName.getText().isEmpty() || txtPassword.getPassword().length == 0
                || txtRePassword.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy dủ các thông tin trước khi thêm người dùng",
                    "Thiếu dữ liệu", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        NguoiDungHeThong test = users.stream().filter(it -> it.getUsername().equals(txtUsername.getText())).findAny()
                .orElse(null);

        if (test != null) {
            JOptionPane.showMessageDialog(null, "Tên đăng nhập đã tồn tại trong hệ thống", "Tên đăng nhập đã tồn tại",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        if (!new String(txtPassword.getPassword()).equals(new String(txtRePassword.getPassword()))) {
            JOptionPane.showMessageDialog(null, "Xác nhận mật khẩu không khớp. Vui lòng kiểm tra lại",
                    "Mật khẩu không khớp", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String hashedPW = Md5Gen.getMD5(new String(txtPassword.getPassword()));

        NguoiDungHeThongDAO.addNew(new NguoiDungHeThong(txtFullName.getText(), txtUsername.getText(), hashedPW));

        JOptionPane.showMessageDialog(null, "Đã thêm người dùng thành công", "Thêm người dùng",
                JOptionPane.INFORMATION_MESSAGE);
        loadData(null);
        btnResetInputActionPerformed(null);

    }// GEN-LAST:event_btnAddUserActionPerformed

    private void btnResetPwActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnResetPwActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn đặt lại mật khẩu cho người dùng đã chọn?",
                "Xác nhận đặt lại mật khẩu", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            NguoiDungHeThongDAO.update(new NguoiDungHeThong(txtUsername.getText(), hashedDefault));
            JOptionPane.showMessageDialog(null, "Đã đặt lại mật khẩu. Mật khẩu mới là '12345678'", "Đặt lại mật khẩu",
                    JOptionPane.INFORMATION_MESSAGE);
        }
        btnResetInputActionPerformed(null);

    }// GEN-LAST:event_btnResetPwActionPerformed

    private void btnDeleteUsersActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteUsersActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa người dùng đã chọn?", "Xác nhận xóa",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            NguoiDungHeThongDAO.delete(txtUsername.getText());
            JOptionPane.showMessageDialog(null, "Đã xóa người dùng", "Xóa người dùng", JOptionPane.INFORMATION_MESSAGE);
        }
        loadData(null);
        btnResetInputActionPerformed(null);
    }// GEN-LAST:event_btnDeleteUsersActionPerformed

    private void btnResetInputActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnResetInputActionPerformed
        txtFullName.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
        txtRePassword.setText("");
        enableTextbox(true);
        btnAddUser.setEnabled(true);
        btnDeleteUsers.setEnabled(false);
        btnResetPw.setEnabled(false);
        jTable1.clearSelection();
    }// GEN-LAST:event_btnResetInputActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnTimKiemActionPerformed
        if (txtTimKiem.getText().isEmpty()) {
            loadData(null);
        } else {
            loadData(txtTimKiem.getText());
        }
    }// GEN-LAST:event_btnTimKiemActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow() == -1) {
            return;
        }

        int index = jTable1.getSelectedRow();

        enableTextbox(false);

        txtFullName.setText(users.get(index).getFull_name());
        txtUsername.setText(users.get(index).getUsername());
        btnResetPw.setEnabled(true);
        btnDeleteUsers.setEnabled(true);
        btnAddUser.setEnabled(false);
    }// GEN-LAST:event_jTable1MouseClicked

    private void enableTextbox(boolean b) {
        txtFullName.setEnabled(b);
        txtUsername.setEnabled(b);
        txtPassword.setEnabled(b);
        txtRePassword.setEnabled(b);
    }

