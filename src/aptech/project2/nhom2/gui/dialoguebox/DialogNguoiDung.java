    private void loadData(String keyword) {
        userList = NguoiDungHeThongDAO.findAll();
        if (keyword == null) {
            users = userList.stream().filter(it -> it.getStatus() == 1 && it.getAdmin() != 1)
                    .collect(Collectors.toList());
        } else {
            users = userList.stream()
                    .filter(it -> it.getStatus() == 1 && it.getAdmin() != 1 && it.getUsername().equals(keyword))
                    .collect(Collectors.toList());
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        users.forEach(it -> {
            model.addRow(new Object[] { it.getFull_name(), it.getUsername(), });
        });
    }

